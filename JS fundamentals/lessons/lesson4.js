// Relaitional or comparison operators

// > - more than
// < - less than
// >= - more than equal
// <= less than equal

var result =  10 > 5
console.log(result)

// Equality operators
var x = 1

// Loose comparison (data type not compared)
console.log(x == '1')

// Strict comparison (data type compared)
console.log(x === '1')